package witharraylist;

import java.util.ArrayList;

public class CustomerTest {

	public static void main(String[] args) {
		
		ArrayList<Customer> customerList = new ArrayList<Customer>();
		
		Customer customerLee = new Customer(10010, "이순신");
		Customer customerKim = new VIPCustomer(10020, "김유신", 21234);
		Customer customerShin = new Customer(10030, "신사임당");
		Customer customerYul = new GoldCustomer(10040, "이율곡");
		Customer customerHong = new GoldCustomer(10050, "홍길동");
		
		customerList.add(customerLee);
		customerList.add(customerKim);
		customerList.add(customerShin);
		customerList.add(customerYul);
		customerList.add(customerHong);
		
		System.out.println("=============고객정보출력");
		
		for(Customer customer : customerList) {
			System.out.println(customer.showCustomerInfo());
		}
		
		System.out.println("============할인율과 보너스 포인트 계산");
		
		int price = 10000;
		for (Customer customer : customerList) {
			int cost = customer.calcPrice(price);
			System.out.println(customer.getCustomerName() + "님이 " + cost + "를 지불하셨습니다.");
			System.out.println(customer.getCustomerName() + "님의 현재 보너스 포인트는 " + customer.bonusPoint + "입니다.");
		}

	}

}
